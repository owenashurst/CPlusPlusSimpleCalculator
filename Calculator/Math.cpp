#include "Math.h"

int Math::Add(int &numberA, int &numberB)
{
	return numberA + numberB;
}

int Math::Subtract(int &numberA, int &numberB)
{
	return numberA - numberB;
}

int Math::Multiply(int &numberA, int &numberB)
{
	return numberA * numberB;
}

int Math::Divide(int &numberA, int &numberB)
{
	return numberA / numberB;
}